void ColorTables::addRGBPoint(unsigned int index, float* colors, float r, float g, float b)
{
	colors[index*3] = r;
	colors[index*3+1] = g;
	colors[index*3+2] = b;
}

void ColorTables::buildTexture(osg::ref_ptr<osg::Texture1D> texture, float* colors)
{
	// Create a new color table image out of the new color table array
	osg::ref_ptr<osg::Image> image = new osg::Image;
	image->setImage(256, 1, 1, GL_RGB, GL_RGB, GL_FLOAT, (unsigned char*)colors, osg::Image::NO_DELETE);

	// Create the color table texture
	texture->setFilter(osg::Texture::MIN_FILTER, osg::Texture::LINEAR);
	texture->setFilter(osg::Texture::MAG_FILTER, osg::Texture::LINEAR);
    texture->setWrap(osg::Texture1D::WRAP_S, osg::Texture1D::CLAMP_TO_EDGE);
	texture->setImage(image.get());
}
